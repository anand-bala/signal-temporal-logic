Expr get_phi() {
  auto theta1 = stl::Predicate("theta") <= 1.0;
  auto theta2 = stl::Predicate("theta") < 0.25;
  auto x_dot  = stl::Predicate("x_dot") < 0.25;
  auto x      = stl::Predicate("x") < 1;
  auto phi    = stl::Always(theta1 & x) & stl::Eventually(stl::Always(theta2 & x_dot));
  return phi;
}

